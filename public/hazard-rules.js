  /**
   * 分析隐患并自动判定等级
   * @param {string} category - 安全类别
   * @param {string} title - 隐患标题
   * @param {string} description - 详细描述
   * @returns {object} { level, law, desc, confidence, matchedKeywords, suggested }
   */
  function analyze(category, title, description) {
    var text = (title + ' ' + description).toLowerCase();
    var rules = RULES[category] || [];

    // 1. 精确匹配规则库
    var bestMatch = null;
    var bestScore = 0;

    for (var i = 0; i < rules.length; i++) {
      var rule = rules[i];
      var matchCount = 0;
      var matchedKws = [];

      for (var j = 0; j < rule.keywords.length; j++) {
        var kw = rule.keywords[j];
        if (text.indexOf(kw.toLowerCase()) !== -1) {
          matchCount++;
          matchedKws.push(kw);
        }
      }

      if (matchCount > 0) {
        var score = matchCount * (rule.level === '重大' ? 3 : rule.level === '就大' ? 2 : 1);
        // 如果等级更高或匹配分数更高，则优先
        if (!bestMatch || score > bestScore ||
            (score === bestScore && levelPriority(rule.level) > levelPriority(bestMatch.level))) {
          bestMatch = {
            level: rule.level,
            law: rule.law,
            desc: rule.desc,
            matchedKeywords: matchedKws,
            confidence: matchCount >= 2 ? 'high' : 'medium'
          };
          bestScore = score;
        }
      }
    }

    if (bestMatch) {
      bestMatch.suggested = true;
      return bestMatch;
    }

    // 2. 通用关键词兜底
    var majorHits = [];
    var sigHits = [];

    for (var k = 0; k < MAJOR_GENERAL_KEYWORDS.length; k++) {
      if (text.indexOf(MAJOR_GENERAL_KEYWORDS[k]) !== -1) {
        majorHits.push(MAJOR_GENERAL_KEYWORDS[k]);
      }
    }
    for (var m = 0; m < SIGNIFICANT_GENERAL_KEYWORDS.length; m++) {
      if (text.indexOf(SIGNIFICANT_GENERAL_KEYWORDS[m]) !== -1) {
        sigHits.push(SIGNIFICANT_GENERAL_KEYWORDS[m]);
      }
    }

    if (majorHits.length > 0) {
      return {
        level: '重大',
        law: '安全生产法 / 消防法相关条款',
        desc: '描述中涉及重大危险因素：' + majorHits.join('、'),
        matchedKeywords: majorHits,
        confidence: 'medium',
        suggested: true
      };
    }

    if (sigHits.length > 0) {
      return {
        level: '较大',
        law: '安全生产法 / 相关行业标准',
        desc: '描述中涉及安全隐患因素：' + sigHits.join('、'),
        matchedKeywords: sigHits,
        confidence: 'low',
        suggested: true
      };
    }

    // 3. 无法自动判定，默认一般
    return {
      level: '一般',
      law: '安全生产法 / 企业安全管理制度',
      desc: '根据描述未匹配到明确的重大或较大隐患关键词，默认判定为一般隐患，建议人工确认',
      matchedKeywords: [],
      confidence: 'low',
      suggested: false
    };
  }

  function levelPriority(level) {
    if (level === '重大') return 3;
    if (level === '较大') return 2;
    return 1;
  }

  /**
   * 获取指定类别的所有规则（用于展示参考）
   */
  function getRules(category) {
    return RULES[category] || [];
  }

  /**
   * 获取指定类别的重大隐患规则（用于提示）
   */
  function getMajorRules(category) {
    return (RULES[category] || []).filter(function(r) { return r.level === '重大'; });
  }

  return {
    analyze: analyze,
    getRules: getRules,
    getMajorRules: getMajorRules
  };

